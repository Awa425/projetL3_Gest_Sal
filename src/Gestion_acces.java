     public ArrayList<Personne> listepersonne(){
       ArrayList<Personne> listepersonne =new ArrayList<>();
       try{
            Connection con = Connexion.getCo();
            String req1="SELECT *from personne" ;     
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(req1);
            Personne personne;
            while(rs.next()){
                personne =new Personne(rs.getInt("id_personne"), rs.getString("prenom"),rs.getString("nom"),rs.getString("mail"),rs.getString("statut"));
                listepersonne.add(personne);
            }
       }
       catch(Exception e){
            JOptionPane.showMessageDialog( null, e);
        }
       return listepersonne;
    }
     
      public void affiche_personne(){
        ArrayList<Personne> list=listepersonne();
        DefaultTableModel model=(DefaultTableModel)liste.getModel();
        Object[] row=new Object[5];
        for(int i=0;i<list.size();i++){
             row[0]=list.get(i).getid_personne(); 
            row[1]=list.get(i).getprenom();
             row[2]=list.get(i).getnom();
             row[3]=list.get(i).getmail();
              row[4]=list.get(i).getstatut();
            model.addRow(row);
            
        }
    }

