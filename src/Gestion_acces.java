        int i = liste.getSelectedRow();
           TableModel model = liste.getModel();
           String id_per = model.getValueAt(i, 0).toString();
           int id_p = Integer.parseInt(id_per);
        
        try{
            Connection con = Connexion.getCo();
            String query="UPDATE personne SET prenom = ?, nom = ?, mail = ?, statut = ? WHERE id_personne = ?";
            
            PreparedStatement pst = con.prepareStatement(query);
            //pst.setInt(5, id_p);
            System.out.println(prenom.getText());
            
            pst.setString(2, nom.getText());
          pst.setString(3,mail.getText());
          pst.setString(4,statut.getSelectedItem().toString());
           pst.setInt(5, id_p);
           
         affiche_personne();
          pst.executeUpdate();
         
           DefaultTableModel modele=(DefaultTableModel)liste.getModel();
          modele.setRowCount(0);
          
         
          
       JOptionPane.showMessageDialog(null,"Modification effectué");
       setfields();
        
         AccueilAdministrateur.Text.setText("Bien inseré dans la base");
            }
